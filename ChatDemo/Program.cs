

/// <summary>
/// 两个整数相加 / 2つの整数を加算する
/// </summary>
/// <param name="a">第一个加数 / 最初の加数</param>
/// <param name="b">第二个加数 / 2番目の加数</param>
/// <returns>返回两数之和 / 2つの数の合計を返す</returns>
static int Add(int a, int b)
{
    return a + b;
}
/// <summary>
/// 两个整数相减 / 2つの整数を減算する
/// </summary>
/// <param name="a">被减数 / 被減数</param>
/// <param name="b">减数 / 減数</param>
/// <returns>返回两数之差 / 2つの数の差を返す</returns>
static int Subtract(int a, int b)
{
    return a - b;
}
